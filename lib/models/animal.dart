class Animal {
  final String nome;
  final String localizacao;
  final String descricao;
  final String contato;
  final String imagem;

  Animal({
    required this.nome,
    required this.localizacao,
    required this.descricao,
    required this.contato,
    required this.imagem,
  });
}
